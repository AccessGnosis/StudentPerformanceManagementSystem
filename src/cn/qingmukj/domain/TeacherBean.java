public class TeacherBean {
	private String uid;
	private String tid;
	private String tname;
	private String tedubag;
	private String tlevel;
	private String tdepart;

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getTid() {
		return tid;
	}

	public void setTid(String tid) {
		this.tid = tid;
	}

	public String getTname() {
		return tname;
	}

	public void setTname(String tname) {
		this.tname = tname;
	}

	public String getTedubag() {
		return tedubag;
	}

	public void setTedubag(String tedubag) {
		this.tedubag = tedubag;
	}

	public String getTlevel() {
		return tlevel;
	}

	public void setTlevel(String tlevel) {
		this.tlevel = tlevel;
	}

	public String getTdepart() {
		return tdepart;
	}

	public void setTdepart(String tdepart) {
		this.tdepart = tdepart;
	}

}
